"""
For + Range
range -> range (start, stop, step)
"""

numeros = range(10)

for numero in numeros:
    print (numero)

print("0000000000000000") #----------------

casas = range (5,10)

for casa in casas:
    print (casa)

print("0000000000000000") #---------------------

itens = range (5, 10, 2)

for item in itens:
    print(item)


