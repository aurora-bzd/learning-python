# Tipo int e float

# int -> Numero inteiros
# O tipo int representa qualquer numero positivo ou negativo. int sem sinal eh considerado positivo
print(11)
print(-11)
print(0)

# float -> Numeros com pontos flutuantes
# O tipo float representa qualquer numero positivo ou negativo com ponto flutuante. float sem sinal eh considerado positivo
print(1.1, 10.11)
print(0.0, -1.5)

# A funcao type mostra o tipo que o Python inferiu ao valor
print(type('Bruna'))
print(type(20))
print(type(-1.5))
print(type(1.1), type(-1.1), type(0.0))


